// An expression asks a question, and the answer is a value.

let x = 5; // This is an expression that evaluates to 5
x; // This is an expression that evaluates to 5
6 + 4; // This is an expression that evaluates to 10
document.getElementById("content"); // This is an expression that evaluates to the element with the specified id

// A statement tells JavaScript to do something. A statement is a piece of code that does something.

let ten = 6 + 4; // This is a statement that assigns the value of 6 + 4 to the variable ten
console.log(ten); // This is a statement that logs the value of ten to the console
let myDeclaredVariable = "I am a variable"; // This is a statement that declares and assigns a string to a variable
console.log(myDeclaredVariable); // This is a statement that logs the value of myDeclaredVariable to the console
let board = document.getElementById("board"); // This is a statement that assigns the element with the specified id to the variable board
board.textContent = "New Content"; // This is a statement that changes the content of the element with the specified id

// In the above example, the expressions are the right-hand side of the statements.
// The expressions are evaluated, and the results are assigned to the variables or used in the statements.
